package gr.aueb.cf.ch3;

/**
 * This app outputs 10 stars (10 times
 * a star).
 *
 * @author Thanasis Chousiadas
 */
public class Stars10App {

    public static void main(String[] args) {
        int i = 1;

        while (i <= 10) {
            System.out.print("* ");
            i++;
        }
    }
}
